class Metodo:
    def __init__(self, nome, chave):
        self.nome = nome
        self.chave = chave
    def setNome(self, nome):
        self.nome = nome
    def setChave(self, chave):
        self.chave = chave
    def getNome(self):
        return self.nome
    def getChave(self):
        return self.chave